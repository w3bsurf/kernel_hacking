int callCount = 0; // Initialize syscall counter
int callToFollow = 5; // Default syscall, read, to count

// Return how many times a tracked syscall
// has been called
int
sys_getreadcount(void)
{
  int reset, follow;
  argint(0, &follow);
  argint(1, &reset);

  if(reset == 1) {
    // Reset counter if argument given
    callCount = 0;
  }
  if(follow > 0 && follow != callToFollow) {
    // Change currently tracked syscall and reset counter
    callToFollow = follow;
    callCount = 0;
  }
  return callCount;
}

int
sys_fork(void)
{
  return fork();
}

int
sys_exit(void)
{
  exit();
  return 0;  // not reached
}

int
sys_wait(void)
{
  return wait();
}

int
sys_kill(void)
{
  int pid;

  if(argint(0, &pid) < 0)
    return -1;
  return kill(pid);
}

int
sys_getpid(void)
{
  return myproc()->pid;
}

int
sys_sbrk(void)
{
  int addr;
  int n;

  if(argint(0, &n) < 0)
    return -1;
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

int
sys_sleep(void)
{
  int n;
  uint ticks0;

  if(argint(0, &n) < 0)
    return -1;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(myproc()->killed){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

// return how many clock tick interrupts have occurred
// since start.
int
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}
